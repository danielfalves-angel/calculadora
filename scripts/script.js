function soma() {
    let numero1 = parseFloat(document.getElementById("N1").value);
    let numero2 = parseFloat(document.getElementById("N2").value);

    let resul = numero1 + numero2
    alert(resul);
}

function multi() {
    let numero1 = parseFloat(document.getElementById("N1").value);
    let numero2 = parseFloat(document.getElementById("N2").value);

    let resul = numero1 * numero2
    alert(resul);
}

function menos() {
    let numero1 = parseFloat(document.getElementById("N1").value);
    let numero2 = parseFloat(document.getElementById("N2").value);

    let resul = numero1 - numero2
    alert(resul);
}

function divi() {
    let numero1 = parseFloat(document.getElementById("N1").value);
    let numero2 = parseFloat(document.getElementById("N2").value);

    let resul = numero1 / numero2
    alert(resul);
}